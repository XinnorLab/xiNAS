# в”Җв”Җ Banner в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
echo -e "${BLUE}"
cat << 'EOF'

    в–Ҳв–Ҳв•—  в–Ҳв–Ҳв•—в–Ҳв–Ҳв•—в–Ҳв–Ҳв–Ҳв•—   в–Ҳв–Ҳв•— в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•— в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—
    в•ҡв–Ҳв–Ҳв•—в–Ҳв–Ҳв•”в•қв–Ҳв–Ҳв•‘в–Ҳв–Ҳв–Ҳв–Ҳв•—  в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•”в•җв•җв–Ҳв–Ҳв•—в–Ҳв–Ҳв•”в•җв•җв•җв•җв•қ
     в•ҡв–Ҳв–Ҳв–Ҳв•”в•қ в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•”в–Ҳв–Ҳв•— в–Ҳв–Ҳв•‘в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•‘в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—
     в–Ҳв–Ҳв•”в–Ҳв–Ҳв•— в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘в•ҡв–Ҳв–Ҳв•—в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•”в•җв•җв–Ҳв–Ҳв•‘в•ҡв•җв•җв•җв•җв–Ҳв–Ҳв•‘
    в–Ҳв–Ҳв•”в•қ в–Ҳв–Ҳв•—в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘ в•ҡв–Ҳв–Ҳв–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘  в–Ҳв–Ҳв•‘в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•‘
    в•ҡв•җв•қ  в•ҡв•җв•қв•ҡв•җв•қв•ҡв•җв•қ  в•ҡв•җв•җв•җв•қв•ҡв•җв•қ  в•ҡв•җв•қв•ҡв•җв•җв•җв•җв•җв•җв•қ
EOF
echo -e "${NC}"
echo -e "  ${GREEN}в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғ${NC}"
echo -e "  ${YELLOW}${BOLD}  High-Performance NAS Provisioning${NC}"
echo -e "  ${GREEN}в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғ${NC}"
echo ""

# в”Җв”Җ Quick Start Guide в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
echo -e "  ${WHITE}в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ${NC}"
echo -e "    ${CYAN}${BOLD}вңЁ  Welcome to xiNAS вҖ” let's get you started!${NC}"
echo -e "  ${WHITE}в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ${NC}"
echo ""
echo -e "  ${WHITE}${BOLD}QUICK START GUIDE${NC}"
echo -e "  ${DIM}в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ${NC}"
echo ""
echo -e "  ${CYAN}${BOLD}в‘ ${NC}  ${WHITE}рҹ“Ҡ Collect System Data${NC}"
echo -e "      ${DIM}Gather hardware info and generate your licensing key${NC}"
echo ""
echo -e "  ${YELLOW}${BOLD}в‘Ў${NC}  ${WHITE}рҹ”‘ Obtain & Enter License${NC}  ${RED}${BOLD}вҳ… Required${NC}"
echo -e "      ${DIM}Send hardware key to${NC} ${CYAN}support@xinnor.io${NC}"
echo -e "      ${DIM}then enter the received license in the menu${NC}"
echo ""
echo -e "  ${CYAN}${BOLD}в‘ў${NC}  ${WHITE}рҹҢҗ Configure Network${NC}  ${DIM}(optional вҖ” skip for auto-detect)${NC}"
echo -e "      ${DIM}Set IP ranges and interfaces for your storage network${NC}"
echo ""
echo -e "  ${CYAN}${BOLD}в‘Ј${NC}  ${WHITE}рҹҡҖ Run Installation${NC}"
echo -e "      ${DIM}Select your profile and let the installer do the rest${NC}"
echo ""
echo -e "  ${DIM}в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ${NC}"
echo -e "  ${DIM}рҹ’¬ Questions?${NC}  ${CYAN}support@xinnor.io${NC}"
echo -e "  ${DIM}в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ${NC}"
echo ""

# в”Җв”Җ Confirm в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
read -p "  Ready to begin? [Y/n] " -n 1 -r
echo ""
if [[ $REPLY =~ ^[Nn]$ ]]; then
    echo ""
    warn "Installation cancelled. Run this script again when ready."
    echo ""
    exit 0
fi

# в”Җв”Җ Step 1: Requirements в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
step "Checking requirements"

if [[ $EUID -ne 0 ]]; then
    fail "${RED}Must be run as root${NC}"
    echo ""
    echo -e "     Run:  ${CYAN}sudo bash <(curl -fsSL URL)${NC}"
    echo ""
    exit 1
fi
ok "Running as root"

if [[ -f /etc/os-release ]]; then
    . /etc/os-release
    if [[ "$ID" != "ubuntu" ]]; then
        warn "xiNAS is designed for Ubuntu вҖ” detected: ${BOLD}$PRETTY_NAME${NC}"
        read -p "     Continue anyway? [y/N] " -n 1 -r
        echo ""
        [[ ! $REPLY =~ ^[Yy]$ ]] && exit 1
    else
        ok "OS: ${BOLD}$PRETTY_NAME${NC}"
    fi
else
    warn "Could not detect OS version"
fi

# в”Җв”Җ Step 1b: Root SSH access в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
step "Configuring root SSH access"

# Ubuntu cloud images (AWS/GCP/Azure) drop a cloud-init config that sets
# PermitRootLogin no. We override it with a lower-numbered drop-in so
# key-based root SSH works for the AI / MCP bridge.
_sshd_dropin="/etc/ssh/sshd_config.d/10-xinas-root-access.conf"
mkdir -p /etc/ssh/sshd_config.d
if [[ ! -f "$_sshd_dropin" ]]; then
    cat > "$_sshd_dropin" <<'SSHEOF'
# Managed by xiNAS installer
# Allows key-based root SSH login (for Claude Code MCP stdio transport)
# Password root login remains blocked by Ubuntu default policy
PermitRootLogin prohibit-password
SSHEOF
    systemctl reload ssh 2>/dev/null || systemctl reload sshd 2>/dev/null || true
    ok "Root SSH key-login enabled (password login stays blocked)"
else
    ok "Root SSH config already present"
fi

# Warn if root has no password вҖ” useful for console recovery and sudo auditing
_root_pw=$(passwd -S root 2>/dev/null | awk '{print $2}')
if [[ "$_root_pw" == "L" || "$_root_pw" == "NP" ]]; then
    warn "Root has no password set вҖ” run ${CYAN}xinas-menu${NC} вҶ’ A вҶ’ 4 to set one"
else
    ok "Root password is set"
fi

# в”Җв”Җ Step 2: Repository в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
INSTALL_DIR="/opt/xiNAS"
REPO_URL="https://github.com/XinnorLab/xiNAS.git"

step "Setting up repository"
info "Target: ${WHITE}${INSTALL_DIR}${NC}"

if ! command -v git &>/dev/null; then
    info "Installing git..."
    apt-get update -qq
    apt-get install -y git -qq
    ok "git installed"
else
    ok "git found"
fi

if [[ -d "$INSTALL_DIR" ]]; then
    info "Existing installation found вҖ” updating..."
    cd "$INSTALL_DIR"
    git fetch origin -q
    git reset --hard origin/main -q
    ok "Repository updated"
else
    info "Cloning repository..."
    git clone -q "$REPO_URL" "$INSTALL_DIR"
    cd "$INSTALL_DIR"
    ok "Repository cloned to ${WHITE}${INSTALL_DIR}${NC}"
fi

chmod +x ./*.sh 2>/dev/null || true

# в”Җв”Җ Step 3: System preparation в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
step "Launching setup wizard"
info "Installing Ansible, yq, and launching the provisioning menu..."
echo ""

./prepare_system.sh

# в”Җв”Җ Done в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
echo ""
_W=55
_bar() { printf 'в•җ%.0s' $(seq 1 $_W); }
echo -e "  ${GREEN}$(_bar)${NC}"
echo -e "    ${GREEN}${BOLD}вң“  xiNAS installed successfully!${NC}"
echo -e ""
echo -e "    ${DIM}Directory :${NC}  ${WHITE}${INSTALL_DIR}${NC}"
echo -e "    ${DIM}Management:${NC}  ${CYAN}sudo xinas-menu${NC}"
echo -e "  ${GREEN}$(_bar)${NC}"
echo ""
